 function DWU_GetRegion(server)
	if GetLocale() ~= 'zhCN' then 
		return "";
	end
	local serverName;
	if not server then 
		serverName = GetRealmName(); 
	else 
		serverName = server;
	end
	
	if serverToRegion and serverToRegion[serverName] then 
		return serverToRegion[serverName].region;
	end

	return "";
end

function DWU_GetBGGroup(server) 
	if GetLocale() ~= 'zhCN' then 
		return;
	end
	local serverName;
	if not server then 
		serverName = GetRealmName();
	else 
		serverName = server;
	end 
	if serverToRegion and serverToRegion[serverName] then 
		local bg = serverToRegion[serverName].bg;
		if bg ~= 0 then 
			return bg;
		end 
	end 
end 

